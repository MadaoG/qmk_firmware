// Runs constantly in the background, in a loop.
void matrix_scan_user(void) {
    uint8_t layer = biton32(layer_state);
    t++;
    if (t >= 1000) {
      t = 0;
    }

    if (layer == last_layer) {
      // update leds
      switch (layer) {
        case 1:
          /* update_led_1(); */
          set_led_r();
          break;
        case 2:
          update_led_2();
          break;
        case 3:
          update_led_3();
          break;
        case 4:
          update_led_1();
          update_led_2();
          break;
        case 5:
          update_led_1();
          update_led_3();
          break;
        case 6:
          update_led_2();
          update_led_3();
          break;
        case 7:
          update_led_1();
          update_led_2();
          update_led_3();
          break;
        default:
          ergodox_board_led_off();
      }
    } else {
      last_layer = layer;
      t = 0;
      // init leds
      ergodox_board_led_off();
      ergodox_right_led_1_off();
      ergodox_right_led_2_off();
      ergodox_right_led_3_off();
    }
};

